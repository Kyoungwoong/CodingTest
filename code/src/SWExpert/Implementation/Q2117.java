package SWExpert.Implementation;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.List;
import java.util.StringTokenizer;

public class Q2117 {
    private static int tc, N, M;
    private static int[][] village;
    private static List<Integer> houses = new ArrayList<>();

    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        tc = Integer.parseInt(br.readLine());

        StringBuilder sb = new StringBuilder();
        StringTokenizer st;
        for (int t = 1; t <= tc; t++) {
            st = new StringTokenizer(br.readLine());
            N = Integer.parseInt(st.nextToken());
            M = Integer.parseInt(st.nextToken());

            village = new int[N][N];
            houses = new ArrayList<>();
            for (int i = 0; i < N; i++) {
                st = new StringTokenizer(br.readLine());
                for (int j = 0; j < N; j++) {
                    village[i][j] = Integer.parseInt(st.nextToken());
                    if (village[i][j] == 1) {
                        houses.add(i * N + j);
                    }
                }
            }

            int ans = -1;
            for (int k = 1; k <= 2 * N; k++) {
                int maxHome = -1;
                // 커버 범위 찾기
                for (int r = 0; r < N; r++) { // 중심 범위
                    for (int c = 0; c < N; c++) {
                        int serviced = 0;
                        for (int house : houses) {
                            int x = house / N;
                            int y = house % N;
                            if (Math.abs(r - x) + Math.abs(c - y) <= k - 1) {
                                serviced++;
                            }
                        }
                        maxHome = Math.max(maxHome, serviced);

                    }
                }

                if (maxHome * M - (k * k + (k - 1) * (k - 1)) >= 0) {
//                    System.out.println("K: " + k + "\thome: " + maxHome + " profit: " + (maxHome * M - (k * k + (k - 1) * (k - 1))));
                    ans = Math.max(ans, maxHome);
                }
            }

            sb.append("#").append(t).append(" ").append(ans).append("\n");
        }
        System.out.println(sb);
    }
}

